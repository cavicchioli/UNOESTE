    private void bntAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAddActionPerformed
         
           
           
          Double totAcumulado, totTotal; 
        
          totAcumulado = Double.parseDouble(0 + txtTotalAcumulado.getText());
          totTotal = Double.parseDouble(txtTotal.getText());             
          
          int qtde = Integer.parseInt(txtQtd.getText());
          
          DefaultTableModel model=(DefaultTableModel)tVenda.getModel();
          
          int flag = 0; 
          
          for(int i=0; i < model.getRowCount(); i++)
          {
              if(Integer.parseInt(model.getValueAt(i, 0)+"") == prod.getProd_codigo())
              {
                  if(!ChecaEstoque(qtde +  Integer.parseInt(model.getValueAt(i, 3).toString())))
                        JOptionPane.showMessageDialog(rootPane, "Quantidade indisponivel");
                  else
                  {
                      model.setValueAt(qtde +  Integer.parseInt(model.getValueAt(i, 3).toString()), i, 3);
                      JOptionPane.showMessageDialog(rootPane, "Produto Atualizado");
                      txtTotalAcumulado.setText(String.valueOf(totAcumulado + totTotal));                      
                      model.setValueAt(Double.parseDouble(model.getValueAt(i, 4).toString()) + (qtde * prod.getProd_valor()) , i, 4);
                  }                  
                  flag = 1;   
              }      
          }
          
          
          
          
          if(flag == 0)
          {
            model.addRow(new String[]{
                prod.getProd_codigo()+"",
                prod.getProd_nome(),
                txtValor.getText(), 
                txtQtd.getText(), 
                txtTotal.getText()}); 

            JOptionPane.showMessageDialog(rootPane, "Produto Adicionado com sucesso");
            txtTotalAcumulado.setText(String.valueOf(totAcumulado + totTotal));
          }         
          txtQtd.setText("");
          txtValor.setText("");
          txtTotal.setText("");
          txtProduto.setText("");
    }//GEN-LAST:event_bntAddActionPerformed

