    private void txtQtdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtQtdFocusLost
                
        int qtde = Integer.parseInt(txtQtd.getText());     
        
        if(!ChecaEstoque(qtde))
        {
            JOptionPane.showMessageDialog(rootPane, "Estoque esgotadado!!!"); 
            txtQtd.setText("");
            txtTotal.setText("");
           // txtQtd.setFocusable(true);
            txtQtd.requestFocus();
        }
        else
        {
             txtTotal.setText(String.valueOf(prod.getProd_valor() * qtde));
             
             
             
        }
            
        
    }//GEN-LAST:event_txtQtdFocusLost

