    private void btnFinalizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizaActionPerformed
       
        Date data = new Date(System.currentTimeMillis());
        
        vend.setCli_codigo(cli.getCli_codigo());
        vend.setFunc_codigo(func.getFunc_codigo());
        vend.setVend_data(null);
        vend.setVend_valor_total(Double.parseDouble(txtTotalAcumulado.getText()));
        
         
         
         DefaultTableModel model=(DefaultTableModel)tVenda.getModel();
         Vendas_produtos [] itens = new Vendas_produtos[model.getRowCount()];
         int cont = 0 ; 
         
         
         
         
         for(int i=0; i < model.getRowCount(); i++)
         {
               itens[cont++] = new Vendas_produtos(vend.getVend_codigo(),
                       Integer.parseInt(model.getValueAt(i, 0).toString()),
                       Integer.parseInt(model.getValueAt(i, 3).toString()), 
                       Double.parseDouble(model.getValueAt(i, 2).toString()));
              // cont++; 
         }
         vend.setVp(itens);
         
         
         if(vend.EfetuarVenda()) 
            JOptionPane.showMessageDialog(rootPane, "Venda Efetuada com sucesso!!");
         else
            JOptionPane.showMessageDialog(rootPane, "erro ao efetuar a venda !");
         
         
         
         model.setNumRows(0);
         txtTotalAcumulado.setText("");
         txtVendedor.setText("");
         txtCliente.setText("");
       
        
    }//GEN-LAST:event_btnFinalizaActionPerformed

