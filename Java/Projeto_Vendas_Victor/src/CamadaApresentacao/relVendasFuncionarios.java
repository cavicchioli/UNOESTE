        private void carregaFuncionarios() throws Exception{  
            cbFuncionarios.removeAllItems();
            List<Funcionarios> l = f.listarFuncionarios();
           
      
        Iterator<Funcionarios> it = l.iterator();  
        while(it.hasNext()){  
            Funcionarios e = it.next();  
            String nome = e.getFis_nome();
            String cod = String.valueOf(e.getFis_codigo());
             cbFuncionarios.addItem(cod+"-"+nome); 
        }  
      
    }  
    
